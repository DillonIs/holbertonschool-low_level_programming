#include <stdio.h>
#include <stdlib.h>

/**
 * op_add - Operation addition
 * @a: First variable
 * @b: Second variable
 *
 * Return: Returns value of operation
 */

int op_add(int a, int b)
{
  return (a + b);
}

/**
 * op_sub - Operation subtraction
 * @a: First variable
 * @b: Second variable
 *
 * Return: Returns value of operation
 */

int op_sub(int a, int b)
{
  return (a - b);
}

/**
 * op_mul - Operation multiplication
 * @a: First variable
 * @b: Second variable
 *
 * Return: Returns value of operation
 */

int op_mul(int a, int b)
{
  return (a * b);
}

/**
 * op_div - Operation division
 * @a: First variable
 * @b: Second variable
 *
 * Return: Returns the value of operation
 */

int op_div(int a, int b)
{
  return (a / b);
}

/**
 * op_mod - Operation modulo
 * @a: First variable
 * @b: Second variable
 *
 * Return: Returns value of operation 
 */

int op_mod(int a, int b)
{
  return (a % b);
}
